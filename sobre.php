            <h2>História da Escola</h2>    
    <p class="text-start">A Escola Estadual de Ensino Fundamental Antenor Navarro, localizada em Guarabira, PB, foi fundada em 9 de março de 1933, pelo Decreto nº 369. A instituição leva o nome em homenagem a uma importante figura paraibana, Antenor Navarro, que faleceu tragicamente em um acidente aéreo na Bahia, em 1932. Na época, ele estava viajando para o Rio de Janeiro, com o objetivo de buscar recursos para enfrentar a seca que assolava o estado da Paraíba.

O prédio da escola foi construído durante a gestão do prefeito José Tertuliano Ferreira de Melo, entre 1932 e 1935. José Soares de Carvalho foi o primeiro diretor da escola, e ao longo dos anos, diversas personalidades assumiram a direção da instituição, incluindo Mário Romero, Lourival Cavalcante, Fenelon Câmara, Geracina Lins de Souza Filha (Didi), Ana Natália Ferreira de Melo (Anita Melo), Stelita Cunha Farias, Jandyra da Silva Araújo, entre outros.

Completando 80 anos em 2013, a escola ainda funciona no mesmo local, na rua Prefeito Manuel Lordão, e continua a oferecer educação de qualidade à comunidade. Sob a administração das professoras Eny Amorim de Almeida Andrade e Nébia de Lucena Souto Marinho, que foram escolhidas por meio de eleições realizadas dentro da escola, a instituição atende atualmente a 522 alunos, distribuídos desde o Ensino Fundamental (do 1º ao 7º ano), Educação de Jovens e Adultos (EJA), até o Ensino Médio. A escola também oferece educação especial por meio da Sala de Recursos Multifuncional e programas como o "Mais Educação", atendendo 150 alunos em horário integral.

Ao longo de sua história, a escola formou diversos alunos ilustres, como o professor Martinho Alves de Andrade, o radialista Pedro Alves, o artista plástico Clóvis Dias Júnior, o procurador regional da República Humberto de Paiva Araújo, o ex-governador Roberto Paulino, o ex-prefeito José Augustinho Souza de Almeida e o atual prefeito Zenóbio Toscano. Além disso, destacam-se também ex-alunos como Aurenice Rufino, Nébia de Lucena e José André Francisco Filho, atual secretário de Cultura, que contribuíram para o crescimento da escola e da cidade.</p>
